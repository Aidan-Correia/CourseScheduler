    private void addSemesterSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addSemesterSubmitButtonActionPerformed

        String semester = addSemesterTextfield.getText();
        if (semester.length() < 1){
             addSemesterStatusLabel.setText("Please enter the semester name.");
            return;
        }
        SemesterQueries.addSemester(semester);
        addSemesterStatusLabel.setText("Semester " + semester + " has been added.");
        rebuildSemesterComboBoxes();
    }//GEN-LAST:event_addSemesterSubmitButtonActionPerformed

    private void addCourseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCourseButtonActionPerformed

        String courseCode = addCourseCodeText.getText();
        String courseDescription = addCourseDescriptionText.getText();
        CourseEntry newCE;
        if (((currentSemester.length() < 1) || (courseCode.length() < 1)) || ((courseDescription.length() < 1) || (addCourseSeatsText.getText().length() < 1))){
            addCourseStatusLabel.setText("Please fill in all fields.");
            return;
        }
        int courseSeats = Integer.parseInt(addCourseSeatsText.getText());
        newCE = new CourseEntry(currentSemester, courseCode, courseDescription, courseSeats);
        CourseQueries.addCourse(newCE);
        addCourseStatusLabel.setText("Course: " + courseCode + " has been added.");
        rebuildCourseComboBoxes();
    }//GEN-LAST:event_addCourseButtonActionPerformed

    private void addStudentSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addStudentSubmitButtonActionPerformed
        String stID = addStudentIDText.getText();
        String stFN = addStudentFirstNameText.getText();
        String stLN = addStudentLastNameText.getText();
        if (((stFN.length() < 1) || (stLN.length() < 1)) || (stID.length() < 1)){
            addStudentStatusLabel.setText("Please fill in all fields.");
            return;
        }
        StudentQueries.addStudent(new StudentEntry(stID, stFN, stLN));
        addStudentStatusLabel.setText("Student :" + stID + " has been added. ");
        rebuildStudentComboBoxes();
    }//GEN-LAST:event_addStudentSubmitButtonActionPerformed

