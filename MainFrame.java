    private void displayScheduleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayScheduleButtonActionPerformed
        Object id = studentComboBox.getSelectedItem();
        String display = "Courses enrolled by student: " + id.toString() + "       for semester: " + currentSemester;
        ArrayList<ScheduleEntry> schedules = new ArrayList<ScheduleEntry>();
        schedules = ScheduleQueries.getScheduleByStudent(currentSemester, id.toString());
        for(ScheduleEntry entry : schedules){
            display += "\n" + entry.getcc();
        }
        displayTextArea.setText(display);
        
    }//GEN-LAST:event_displayScheduleButtonActionPerformed

    private void displayCoursesButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayCoursesButtonActionPerformed
        // TODO add your handling code here:
        String display = "Courses available for semester: " + currentSemester;
        ArrayList<CourseEntry> courses = new ArrayList<CourseEntry>();
        courses = CourseQueries.getAllCourses(currentSemester);
        for(CourseEntry entry : courses){
            display += "\nCourse: " + entry.getCourseCode() + "      " + entry.getDescription() + "       Seats: " + entry.getSeats();
        }
        displayTextArea.setText(display);
    }//GEN-LAST:event_displayCoursesButtonActionPerformed

    private void scheduleCourseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scheduleCourseButtonActionPerformed
            Object id = studentComboBox.getSelectedItem();
            Object course = courseComboBox.getSelectedItem();
            java.sql.Timestamp newTS = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());
            ScheduleEntry newSE;
            if (ScheduleQueries.getScheduledStudentCount(currentSemester, course.toString()) >= CourseQueries.getCourseSeats(currentSemester, course.toString())){ 
                newSE = new ScheduleEntry(currentSemester, course.toString(), id.toString(), "W", newTS);
                ScheduleQueries.addScheduleEntry(newSE);
                scheduleStatusLabel.setText("Student: " + id.toString() + " has been waitlisted for course: " + course.toString());
            }
              else{
                newSE = new ScheduleEntry(currentSemester, course.toString(), id.toString(), "E", newTS);  
                ScheduleQueries.addScheduleEntry(newSE);
                scheduleStatusLabel.setText("Student: " + id.toString() + " has been scheduled for course: " + course.toString());
                      }
            
    
           
                
    }//GEN-LAST:event_scheduleCourseButtonActionPerformed

